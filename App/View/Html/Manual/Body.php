<section class="c-modal dinamico" data-model="dinamico">
    <p>
    <i class="fas fa-window-close"></i>
    <strong>Microfones dinâmicos</strong>Os também chamados microfones duros, baseados numa bobina 
        móvel acoplada à membrana, têm em geral uma resposta um tanto “dura”, só captando 
        bem a fonte sonora próxima à membrana. Esses modelos são os mais recomendados para 
        captar sons de maior intensidade ou pressão, como percussão, metais (sopros) e 
        alto-falantes de guitarra. Com sua resposta fraca para sons mais distantes, são 
        muito úteis para os pequenos estúdios sem tratamento acústico, assim como para o 
        uso no palco, já que são resistentes a ruídos de manuseio.
    </p>
</section>

<section class="c-modal condensador" data-model="condensador">
    <p>
    <i class="fas fa-window-close"></i>
    <strong>Microfones a condensador</strong>Bem mais sensíveis que os dinâmicos, são mais usados em 
        estúdios de gravação e de TV, otimizando a qualidade sonora. Ideais para captar 
        vozes e instrumentos com sons de média ou pouca intensidade, desde que num ambiente 
        acústico tratado. Também chamados de microfones a eletreto ou capacitivos, esses 
        modelos precisam de energia elétrica para alimentar o condensador.
    </p>
</section>

